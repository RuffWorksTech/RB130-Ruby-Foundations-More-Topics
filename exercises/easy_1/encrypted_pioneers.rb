def rot13(encrypted_char)
  case encrypted_char
  when 'a'..'m', 'A'..'M' then (encrypted_char.ord + 13).chr
  when 'n'..'z', 'N'..'Z' then (encrypted_char.ord - 13).chr
  else encrypted_char
  end
end

def decrypt_name(name)
  name.chars.map do |char|
    rot13(char)
  end.join
end

